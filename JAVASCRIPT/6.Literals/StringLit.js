// Array literals in JavaScript provide a convenient way to create
// arrays with initial values. They are defined using square brackets
// [] and can contain any number of elements separated by commas.

  
let colors = ['orange', 'red', 'aqua', 'green', 'blue'];

console.log(colors);


// Array literals are flexible and can hold elements of different types, 
// including numbers, strings, objects, and even other arrays.


let container = [2, "laptop", true, "papers", "watch"];

console.log(container);
